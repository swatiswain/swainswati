public class Gadgets
{
	private double price;
	private String company;
	
	
	
	
	public  Gadgets(double price, String company)
	
	{
		this.price = price;
		this.company = company;
	}
	
	public double getPrice()
	{
		return price;
	}
	
	
	public String getCompany()
	{
		
		return company;
		
	}

	



	






}